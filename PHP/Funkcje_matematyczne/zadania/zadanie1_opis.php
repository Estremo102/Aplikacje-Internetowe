<h3>Funkcja pi()</h3>
<p>Funkcja pi() zwraca wartość PI:</p>
<pre><code class="language-php">echo(pi());</code></pre>
<pre><code><?php echo(pi()); ?></code></pre>
<p>
    Twoim zadaniem jest policzenie i wyświetlenie obwodu koła. Napisz skrypt w którym:
    <ul>
        <li>utworzysz zmienną $r i przypiszesz do niej wartość 5</li>
        <li>utworzysz zmienną $pole_kola i zapiszesz w nim  wynik wzoru &pi;r<sup>2</sup></li>
        <li>wyświetlisz napis: "pole koła o promieniu 5 wynosi {$pole_kola}"</li>
    </ul>
</p>