<h3>Funkcje min() i max()</h3>
<p>Funkcje min() i max() zwracają najmniejszą lub największą wartość z podanych argumentów</p>
<pre><code class="language-php">echo(min(0, 150, -8, -200) . '&lt;br&gt;');
echo(max(0, 150, -8, -200));</code></pre>
<pre><code><?php echo(min(0, 150, -8, -200) . '<br>');
echo(max(0, 150, -8, -200)); ?></code></pre>
<p>Jako argument funkcji można przekazać także tablicę. W kodzie masz dostęp do zmiennej $tablica. Wypisz najmniejszą oraz największą wartość.</p>
