<h3>wartość całkowita zmiennej</h3>
<p>Funkcja intval() służy do pobierania wartości całkowitej zmiennej.</p>
<pre><code class="language-php">$x = 23465.768;
echo intval($x);

echo "&lt;br&gt;";

$x = "23465.768";
echo intval($x);</code></pre>
<h4>Wynik:</h4>
<pre><code><?php $x = 23465.768;
echo intval($x);

echo "<br>";

$x = "23465.768";
echo intval($x);?></code></pre>

<p>Twoim zadaniem jest przekonwertować zmienną $x do typu całkowitego i zapisać ją do zmiennej $x_converted.</p>
